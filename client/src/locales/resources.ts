export type AppLanguage = "zh-CN" | "zh-TW" | "en" | "ja" | "ko";

type TranslationTree = Record<string, unknown>;

type LocaleResource = {
  translation: TranslationTree;
};

export const SUPPORTED_LANGUAGES: AppLanguage[] = ["zh-CN", "zh-TW", "en", "ja", "ko"];

export const LANGUAGE_OPTIONS: { code: AppLanguage; short: string; label: string }[] = [
  { code: "zh-CN", short: "简", label: "简体中文" },
  { code: "zh-TW", short: "繁", label: "繁體中文" },
  { code: "en", short: "EN", label: "English" },
  { code: "ja", short: "日", label: "日本語" },
  { code: "ko", short: "한", label: "한국어" },
];

export function normalizeLanguage(input?: string | null): AppLanguage {
  const value = (input || "").toLowerCase();

  if (value.startsWith("zh-tw") || value.startsWith("zh-hk") || value.startsWith("zh-mo") || value.includes("hant")) {
    return "zh-TW";
  }
  if (value.startsWith("zh")) {
    return "zh-CN";
  }
  if (value.startsWith("ja")) {
    return "ja";
  }
  if (value.startsWith("ko")) {
    return "ko";
  }
  return "en";
}

